const month = 3 

switch (month) {
    case 1:
        console.log("january");
        break;
    case 2:
        console.log("Feb");
        break;
    case 3:
        console.log("March");
        break;
    case 4:
        console.log("april");
        break;

    default:
        console.log("default catch");
        break;
}

const week = "wed" 
switch (week) {
    case "mon":
        console.log("monday");
        break;
    case "tue":
        console.log("tuesday");
        break;
    case "wed":
        console.log("wednesday");
        break;
    case "thru":
        console.log("thrusday");
        break;

    default:
        console.log("default catch");
        break;
}